bool oled_task_user(void) {
    uint16_t now = timer_read();

    if ((now - last_input_time) > OLED_TIMEOUT) {
        default_oled();
        return false;
    }
  switch (get_highest_layer(layer_state)) {
        case _HYPRLAND:
            hypr_oled();
            break;
        case _VIM:
            vim_oled();
            break;
        case _GENERAL:
            general_oled();
            break;
        case _FN:
            fn_oled();
            break;
        case _NUMPAD:
            numpad_oled();
            break;
        default:
            default_oled();
            break;
    }
  return false;
}
